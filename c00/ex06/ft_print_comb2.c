#include <unistd.h>

void ft_print_comb2(void)
{
	char a1 = '0', a2 = '0', b1 = '0', b2 = '1';

	while(a1 != '9' && a2 != '9')
	{
		write(1, &a1, 1);
		write(1, &a2, 1);
		write(1, " ", 1);
		write(1, &b1, 1);
		write(1, &b2, 1);
		write(1, ", ", 2);
		if (b2 != '9')
			b2++;
		else
		{
			b1++;
			b2 = '0';
		}
		if(b1 == '9' && b2 == '9')
		{
			a2++;
			b1 = '0';
			b2 = a2 + 1;
		}
	}
}

int main()
{
	ft_print_comb2();
	return 0;
}
